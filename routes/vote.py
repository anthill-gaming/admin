from tornado.web import url
from anthill.framework.utils.urls import include, root
from admin.handlers import vote as handlers, LogRequestHandler


@root(pattern=r'^/vote/', namespace='vote')
def route_patterns():
    return [
        url(r'^/?$', handlers.IndexHandler, name='index'),
        url(r'^/log/?$', LogRequestHandler, {'service_name': 'vote'}, name='log')
    ]
